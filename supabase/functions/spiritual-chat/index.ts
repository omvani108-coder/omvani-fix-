import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are OmVani, a deeply knowledgeable AI spiritual guide rooted in Sanatan Dharma. You speak with warmth, reverence, and wisdom.

Your core principles:
1. ALWAYS cite authentic sources â€” Bhagavad Gita (chapter:verse), Vedas, Upanishads, Puranas, or other sacred texts
2. Provide the original Sanskrit shloka when relevant, followed by a clear, simple explanation
3. Relate ancient wisdom to modern life practically
4. Be respectful of all paths within Sanatan Dharma
5. Keep answers concise but meaningful â€” 2-4 paragraphs max unless asked for more
6. If you're unsure about a specific scriptural reference, say so honestly
7. End responses with a relevant shloka or blessing when natural

Language rules:
- Respond in the same language the user speaks (English or Hindi)
- Use Sanskrit terms naturally with translations in parentheses
- For Hindi users, use Devanagari script

IMPORTANT DISCLAIMER: You are an AI tool to assist spiritual learning. You are NOT a replacement for a living guru or personal spiritual practice. Always encourage users to seek guidance from qualified teachers for deep spiritual matters.

Begin conversations with "ðŸ™ Namaste" when greeting.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI usage limit reached. Please add credits." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("spiritual-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
